    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
    // Membuat JTextField untuk memasukkan ID mahasiswa
    JTextField idField = new JTextField(5); // Parameter 5 menunjukkan panjang kolom teks

    // Membuat panel untuk menampung komponen input
    JPanel panel = new JPanel(); 
    panel.add(new JLabel("Masukkan ID yang ingin dihapus:")); // Label sebagai petunjuk untuk pengguna
    panel.add(idField); // Menambahkan text field ke panel

    // Menampilkan dialog konfirmasi dengan input field
    int result = JOptionPane.showConfirmDialog(
        null,                       // Parent component (null = muncul di tengah layar)
        panel,                      // Komponen yang akan ditampilkan (panel berisi label + text field)
        "Hapus Mahasiswa",          // Judul jendela dialog
        JOptionPane.OK_CANCEL_OPTION, // Menampilkan tombol OK dan Cancel
        JOptionPane.PLAIN_MESSAGE     // Tipe pesan sederhana tanpa ikon
    );

    // Mengecek apakah pengguna menekan tombol OK
    if (result == JOptionPane.OK_OPTION) {
        try {
            // Mengambil teks dari text field dan mengubahnya menjadi integer
            int id = Integer.parseInt(idField.getText());
        
        // Memanggil method deleteMhs di controller untuk menghapus data dari database
        controller.deleteMhs(id);
        
        // Menampilkan pesan sukses jika data berhasil dihapus
            JOptionPane.showMessageDialog(
                null, 
                "Data berhasil dihapus.", 
                "Sukses", 
                JOptionPane.INFORMATION_MESSAGE
            );
        } catch (NumberFormatException e) {
            // Menangani error jika input ID bukan angka
            JOptionPane.showMessageDialog(
                null, 
                "ID harus berupa angka.", 
                "Error", 
                JOptionPane.ERROR_MESSAGE
            );
        }
    }
    }//GEN-LAST:event_btnHapusActionPerformed

